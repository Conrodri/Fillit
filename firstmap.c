#include "fillit.h"

char	**firstmap(char **map)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (j < 200)
	{
		while (i < 200)
		{
			map[j][i] = '.';
			i++;
		}
		j++;
		i = 0;
	}
	return (map);
}
